namespace Aurochses.IdentityServer.WebSite.App.Project
{
    /// <summary>
    /// Project options.
    /// </summary>
    public class ProjectOptions
    {
        /// <summary>
        /// Gets or sets the name.
        /// </summary>
        /// <value>
        /// The name.
        /// </value>
        public string Name { get; set; }

        /// <summary>
        /// Gets or sets the support email.
        /// </summary>
        /// <value>
        /// The support email.
        /// </value>
        public string SupportEmail { get; set; }

        /// <summary>
        /// Gets or sets the support URL.
        /// </summary>
        /// <value>
        /// The support URL.
        /// </value>
        public string SupportUrl { get; set; }

        /// <summary>
        /// Gets or sets the account URL.
        /// </summary>
        /// <value>
        /// The account URL.
        /// </value>
        public string AccountUrl { get; set; }
    }
}